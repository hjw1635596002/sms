package com.briup.apps.sms.bean;
/**
 * 角色与用户关系
 * @author lenovo
 *
 */
public class UserRole {
	private  Long id;
	private  Long userId;
	private  Long roleId;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public Long getRoleId() {
		return roleId;
	}
	public void setRoleId(Long roleId) {
		this.roleId = roleId;
	}
	
	
	
	
}
